const relojMujer = document.querySelector("#productos-relojes");
const perfumeMujer = document.querySelector("#productos-perfumes");
const billeteraMujer = document.querySelector("#productos-billeteras");



/**Funcion reloj mujer*/

function renderRelojMujer () {

    for (let info of reloj) {

        let miNodo = document.createElement('div');
        miNodo.classList.add('card-reloj');

        let miNodoCardBody = document.createElement('div');
        miNodoCardBody.classList.add('card-body');

        let miNodoTitle = document.createElement('h5');
        miNodoTitle.classList.add('card-title');
        miNodoTitle.textContent = info['nombre'];

        let miNodoImagen = document.createElement('img');
        miNodoImagen.classList.add('img-fluid');
        miNodoImagen.setAttribute('src', info['imagen']);

        miNodoCardBody.appendChild(miNodoImagen);
        miNodoCardBody.appendChild(miNodoTitle);
        miNodo.appendChild(miNodoCardBody);
        relojMujer.appendChild(miNodo);
    }
}

